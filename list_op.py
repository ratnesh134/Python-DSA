# Creating a list

lst = [1,2,3,4]

# List are mutable, meaning the content inside the list can be changed.

lst[0] = 200

# Add new items
# append adds a 1 item at a time
lst.append(100)


# to append multiple items at a time we can use the extend keyword

lst.extend(300,400,500,600)

# to insert an item at a particular location we can use insert keyword

lst.insert(2,"Hello")

